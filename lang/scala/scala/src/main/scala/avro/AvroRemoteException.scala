package avro

import java.io.IOException

class AvroRemoteException extends IOException {
  private var value:Any = _

  def this(value: Any) {
    this()
    new IOException(value.toString)
    this.value = value
  }

  def this(value: Throwable) {
    this(value.toString)
    initCause(value)
  }

  def this(value: Any, cause: Throwable) {
    this()
    new IOException(value.toString, cause)
    this.value = value
  }

  def getValue: Any = value
}
